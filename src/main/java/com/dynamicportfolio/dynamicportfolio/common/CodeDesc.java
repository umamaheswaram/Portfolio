package com.dynamicportfolio.dynamicportfolio.common;

public class CodeDesc {
  private int code;
  private String desc;

  public CodeDesc() {
  }

  public int getCode() {
    return this.code;
  }

  public void setCode(int code) {
    this.code = code;
  }

  public String getDesc() {
    return this.desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }
}
